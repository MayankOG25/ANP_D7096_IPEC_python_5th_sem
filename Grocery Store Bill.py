# Program to calculate total cost of rice packets

# Input: Price per packet and number of packets
price_per_packet = float(input("Enter the price per packet: "))
number_of_packets = int(input("Enter the number of packets: "))

# Calculation: Total bill amount
total_bill = price_per_packet * number_of_packets

# Output: Display the total bill
print("Total Bill Amount = ₹", total_bill)

